// drop down buttons

function dropDown1() {
	var x1 = document.getElementById('dd1');
	var y1 = document.getElementById('plus1');
	var x2 = document.getElementById('dd2');
	var x3 = document.getElementById('dd3');
	var x4 = document.getElementById('dd4');
	var x5 = document.getElementById('dd5');
	var y2 = document.getElementById('plus2');
	var y3 = document.getElementById('plus3');
	var y4 = document.getElementById('plus4');
	var y5 = document.getElementById('plus5');
	if (x1.style.display === 'block') {
		x1.style.display = 'none';
		y1.innerHTML = '＋';
	} else {
		x1.style.display = 'block';
		y1.innerHTML = '✕';
		x2.style.display = 'none';
		x3.style.display = 'none';
		x4.style.display = 'none';
		x5.style.display = 'none';
		y2.innerHTML = '＋';
		y3.innerHTML = '＋';
		y4.innerHTML = '＋';
		y5.innerHTML = '＋';
	}
}

